public class ReverseArr {
    public static void main(String[] args) {

    }


    //reverse arr
    public static void RevArr() {


        int[] arr = {1, 2, 3, 4};

        for (int i = arr.length - 1; i >= 0; i--) {
            System.out.print(arr[i] + " ");
        }
    }
}

